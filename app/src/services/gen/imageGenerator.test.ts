import { describe, it, expect } from 'vitest';
import { makeImageGenerator, type ImageGenFn, type ImageProbe, type ImageDescriber } from './imageGenerator';
import type { ImageGenOutput } from '../../providers/openaiImage';
import type { EvalCase, Timing } from '../../shared/types';
import type { MediaSignals, ImageSignals } from '../../shared/media';

/** Narrow the generator's union return to image signals for assertions. */
function asImage(signals: MediaSignals): ImageSignals {
  if (signals.kind !== 'image') throw new Error(`expected image signals, got ${signals.kind}`);
  return signals;
}

const timing: Timing = { ttfbMs: 100, totalMs: 100, tokens: 0, tokensPerSec: 0 };
const bytes = new Uint8Array([1, 2, 3]);

function output(over: Partial<ImageGenOutput> = {}): ImageGenOutput {
  return { images: [{ bytes, mime: 'image/png' }], format: 'png', safetyBlocked: false, timing, ...over };
}

const okProbe: ImageProbe = async () => ({ decoded: true, width: 512, height: 512 });

const imageCase: EvalCase = {
  id: 'i1', category: 'typical', input: 'a red cube', pinned: false,
  media: { kind: 'image', width: 512, height: 512, count: 1 },
};

describe('makeImageGenerator', () => {
  it('should compose the prompt, pass size/n, and build metric-gate signals from the probe', async () => {
    let req: { prompt: string; n?: number; size?: string } | null = null;
    const generate: ImageGenFn = async (r) => {
      req = r;
      return output();
    };
    const gen = makeImageGenerator({ generate, probe: okProbe, apiKey: 'sk', model: 'gpt-image-1' });
    const r = await gen(imageCase, { systemPrompt: 'You are an illustrator.' });
    const signals = asImage(r.signals);
    expect(req!.prompt).toBe('You are an illustrator.\n\na red cube');
    expect(req!.n).toBe(1);
    expect(req!.size).toBe('512x512');
    expect(signals).toMatchObject({ decoded: true, width: 512, height: 512, format: 'png', count: 1 });
    expect(signals.labels).toEqual([]); // no describer → empty content signals
    expect(r.timing.totalMs).toBe(100);
  });

  it('should fill labels/ocr from an optional describer', async () => {
    const describe: ImageDescriber = async () => ({ labels: ['a red cube'], ocrText: 'SALE' });
    const gen = makeImageGenerator({ generate: async () => output(), probe: okProbe, describe, apiKey: 'sk', model: 'gpt-image-1' });
    const signals = asImage((await gen(imageCase, { systemPrompt: '' })).signals);
    expect(signals.labels).toEqual(['a red cube']);
    expect(signals.ocrText).toBe('SALE');
  });

  it('should emit safety-blocked signals (decoded:false) without probing', async () => {
    let probed = false;
    const probe: ImageProbe = async () => {
      probed = true;
      return { decoded: true, width: 1, height: 1 };
    };
    const gen = makeImageGenerator({ generate: async () => output({ images: [], safetyBlocked: true }), probe, apiKey: 'sk', model: 'gpt-image-1' });
    const signals = asImage((await gen(imageCase, { systemPrompt: '' })).signals);
    expect(signals.safetyBlocked).toBe(true);
    expect(signals.decoded).toBe(false);
    expect(probed).toBe(false);
  });

  it('should fire onArtifact with the generated bytes (for ephemeral thumbnails)', async () => {
    const seen: Array<{ id: string; len: number }> = [];
    const gen = makeImageGenerator({
      generate: async () => output(),
      probe: okProbe,
      apiKey: 'sk',
      model: 'gpt-image-1',
      onArtifact: (id, img) => seen.push({ id, len: img.bytes.length }),
    });
    await gen(imageCase, { systemPrompt: '' });
    expect(seen).toEqual([{ id: 'i1', len: bytes.length }]);
  });

  it('should NOT fire onArtifact when generation was safety-blocked', async () => {
    let fired = false;
    const gen = makeImageGenerator({
      generate: async () => output({ images: [], safetyBlocked: true }),
      probe: okProbe,
      apiKey: 'sk',
      model: 'gpt-image-1',
      onArtifact: () => (fired = true),
    });
    await gen(imageCase, { systemPrompt: '' });
    expect(fired).toBe(false);
  });

  it('should omit size/n when the case has no image expectation', async () => {
    let req: { n?: number; size?: string } | null = null;
    const bare: EvalCase = { id: 'b', category: 'typical', input: 'sunset', pinned: false, media: { kind: 'image' } };
    const gen = makeImageGenerator({ generate: async (r) => { req = r; return output(); }, probe: okProbe, apiKey: 'sk', model: 'gpt-image-1' });
    await gen(bare, { systemPrompt: 'sys' });
    expect(req!.n).toBeUndefined();
    expect(req!.size).toBeUndefined();
  });
});
