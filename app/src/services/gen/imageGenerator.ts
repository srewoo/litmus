/**
 * Image MediaGenerator (ADR 0007): wires a generation backend + a decode/probe
 * step (+ an optional vision describer) into the injected `MediaGenerator` seam
 * the run loop consumes. Pure orchestration — generation, decoding, and content
 * description are all injected, so this is unit-tested with stubs and no network
 * or DOM. The probe fills the metric-gate signals (decode, dimensions); the
 * OPTIONAL describer fills the content signals (labels, OCR). Without a describer,
 * labels/ocr are empty — gate checks still work fully; object/text checks simply
 * have nothing to match, which the pack reports honestly.
 */
import type { EvalCase, Timing } from '../../shared/types';
import type { ImageSignals } from '../../shared/media';
import type { ChatCallOptions } from '../../providers/types';
import type { ImageGenRequest, ImageGenOutput, GeneratedImage } from '../../providers/openaiImage';
import type { MediaGenerator, MediaGenContext } from '../mediaRun';

export type ImageGenFn = (req: ImageGenRequest, opts: ChatCallOptions) => Promise<ImageGenOutput>;

/** Decode + measure an image's bytes (browser: createImageBitmap; injected for tests). */
export type ImageProbe = (bytes: Uint8Array, mime: string) => Promise<{ decoded: boolean; width: number; height: number }>;

/** Optional vision checker filling content signals (labels/OCR) — a BYOK model call. */
export type ImageDescriber = (bytes: Uint8Array, mime: string) => Promise<{ labels: readonly string[]; ocrText: string }>;

export interface ImageGeneratorDeps {
  readonly generate: ImageGenFn;
  readonly probe: ImageProbe;
  readonly describe?: ImageDescriber;
  readonly apiKey: string;
  readonly model: string;
  readonly fetchImpl?: ChatCallOptions['fetchImpl'];
  readonly clock?: ChatCallOptions['clock'];
  /**
   * Called with the generated bytes for a case (ADR 0007). The UI uses this to
   * show ephemeral thumbnails WITHOUT persisting bytes — image bytes are large and
   * would blow the chrome.storage.local quota (ADR 0004), so they never enter
   * CaseResult/version history. Fired only when an image was produced.
   */
  readonly onArtifact?: (caseId: string, image: GeneratedImage) => void;
}

/** The image prompt under test = the system prompt applied to the case input. */
function composePrompt(systemPrompt: string, input: string): string {
  return [systemPrompt.trim(), input.trim()].filter((s) => s.length > 0).join('\n\n');
}

function sizeOf(exp: { width?: number; height?: number }): string | undefined {
  return exp.width && exp.height ? `${exp.width}x${exp.height}` : undefined;
}

export function makeImageGenerator(deps: ImageGeneratorDeps): MediaGenerator {
  return async (evalCase: EvalCase, ctx: MediaGenContext): Promise<{ signals: ImageSignals & { kind: 'image' }; timing: Timing }> => {
    const exp: { width?: number; height?: number; count?: number } =
      evalCase.media?.kind === 'image' ? evalCase.media : {};
    const opts: ChatCallOptions = {
      apiKey: deps.apiKey,
      ...(deps.fetchImpl ? { fetchImpl: deps.fetchImpl } : {}),
      ...(deps.clock ? { clock: deps.clock } : {}),
      ...(ctx.signal ? { signal: ctx.signal } : {}),
    };
    const req: ImageGenRequest = {
      model: deps.model,
      prompt: composePrompt(ctx.systemPrompt, evalCase.input),
      ...(exp.count ? { n: exp.count } : {}),
      ...(sizeOf(exp) ? { size: sizeOf(exp)! } : {}),
    };
    const out = await deps.generate(req, opts);

    if (out.safetyBlocked || out.images.length === 0) {
      const signals: ImageSignals & { kind: 'image' } = {
        kind: 'image', decoded: false, width: 0, height: 0, format: out.format,
        count: out.images.length, safetyBlocked: out.safetyBlocked, labels: [], ocrText: '',
      };
      return { signals, timing: out.timing };
    }

    const first = out.images[0]!;
    deps.onArtifact?.(evalCase.id, first);
    const [probed, described] = await Promise.all([
      deps.probe(first.bytes, first.mime),
      deps.describe ? deps.describe(first.bytes, first.mime) : Promise.resolve({ labels: [] as readonly string[], ocrText: '' }),
    ]);
    const signals: ImageSignals & { kind: 'image' } = {
      kind: 'image',
      decoded: probed.decoded,
      width: probed.width,
      height: probed.height,
      format: out.format,
      count: out.images.length,
      safetyBlocked: false,
      labels: described.labels,
      ocrText: described.ocrText,
    };
    return { signals, timing: out.timing };
  };
}
